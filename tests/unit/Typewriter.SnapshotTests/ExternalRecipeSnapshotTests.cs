    public async Task GenerateAsyncMatchesExternalRecipeSnapshot(
        string recipeRelativePath,
        string snapshotDirectoryRelativePath,
        bool includeStaticConstants)
    {
        var repositoryRoot = FindRepositoryRoot();
        var recipePath = ResolveExternalRecipePath(repositoryRoot: repositoryRoot, relativePath: recipeRelativePath);
        if (!File.Exists(path: recipePath))
        {
            return;
        }

        var workspace = CreateWorkspaceDirectory();
        try
        {
            await WriteRecipeSampleProjectAsync(workspace: workspace, includeStaticConstants: includeStaticConstants);
            var templatePath = Path.Combine(path1: workspace, path2: "Recipe.tst");
#pragma warning disable SEC0116
            File.Copy(sourceFileName: recipePath, destFileName: templatePath, overwrite: true);
#pragma warning restore SEC0116

            var generator = new TypewriterGenerator(
                templateDiscovery: new FileSystemTemplateDiscovery(),
                metadataProvider: new CSharpProjectMetadataProvider(),
                fileWriter: new FileSystemGeneratedFileWriter());
            var configuration = TypewriterConfiguration.Default with
            {
                Output = TypewriterConfiguration.Default.Output with
                {
                    DryRun = true,
                    Newline = "lf",
                },
            };

            var result = await generator.GenerateAsync(
                request: new GenerationRequest(
                    WorkspacePath: workspace,
                    ProjectPath: Path.Combine(path1: workspace, path2: "RecipeSnapshotProject.csproj"),
                    TemplatePath: templatePath,
                    Mode: GenerationMode.Generate,
                    Configuration: configuration),
                cancellationToken: CancellationToken.None);

            result.Success.Should().BeTrue(because: FormatDiagnostics(diagnostics: result.Diagnostics));
            result.GeneratedFiles.Should().NotBeEmpty();

            var actualFiles = new Dictionary<string, string>(comparer: StringComparer.Ordinal);
            foreach (var generatedFile in result.GeneratedFiles)
            {
                Path.GetDirectoryName(path: generatedFile.Path).Should().Be(workspace);
                generatedFile.Content.Should().NotContain("\r\n");
                var fileName = Path.GetFileName(path: generatedFile.Path);
                actualFiles.Should().NotContainKey(fileName, because: $"Duplicate generated file name: {fileName}");
                actualFiles.Add(key: fileName, value: generatedFile.Content);
            }

            var snapshotDirectory = Path.Combine(
                repositoryRoot,
                "tests",
                "unit",
                "Typewriter.SnapshotTests",
                "ExternalRecipeSnapshots",
                NormalizePath(relativePath: snapshotDirectoryRelativePath));
            if (ShouldAcceptSnapshots())
            {
                if (Directory.Exists(path: snapshotDirectory))
                {
                    Directory.Delete(path: snapshotDirectory, recursive: true);
                }

                Directory.CreateDirectory(path: snapshotDirectory);
                foreach (var (fileName, content) in actualFiles)
                {
                    await File.WriteAllTextAsync(
                        path: Path.Combine(path1: snapshotDirectory, path2: fileName),
                        contents: content);
                }
            }

            Directory.Exists(path: snapshotDirectory).Should().BeTrue(because: $"Snapshot directory does not exist: {snapshotDirectory}");
            var expectedFiles = new Dictionary<string, string>(comparer: StringComparer.Ordinal);
            foreach (var expectedPath in Directory.GetFiles(path: snapshotDirectory))
            {
                expectedFiles.Add(
                    key: Path.GetFileName(path: expectedPath),
                    value: await File.ReadAllTextAsync(path: expectedPath));
            }

            actualFiles.Keys.Order(comparer: StringComparer.Ordinal).Should().Equal(expectedFiles.Keys.Order(comparer: StringComparer.Ordinal));
            foreach (var (fileName, expectedContent) in expectedFiles)
            {
                NormalizeLineEndings(value: actualFiles[key: fileName]).Should().Be(NormalizeLineEndings(value: expectedContent));
            }
        }
        finally
        {
            await DeleteDirectoryWithRetryAsync(directory: workspace);
        }
    }

